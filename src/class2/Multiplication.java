package class2;

public class Multiplication {
    public static void main(String[] args) {
        float number1=10.5f;
        float number2=10.5f;
        double number3=3.9;
        System.out.println("the addition of 2 numbers 10.5 and 10.5 is equal to " +(number1+number2));
        System.out.println("the subtraction of 2 numbers 10.5 and 10.5 is equal to "+(number1-number2));
        System.out.println("the multiplication of 2numbers 10.5 and 10.5 is equal to " +(number1*number2));
        System.out.println("the division of 2 numbers 10.5 and 10.5 is equal to "+ (number1/number2));
        System.out.println("the square of the 3.9 is equal to " + (number3*number3));
    }
}
